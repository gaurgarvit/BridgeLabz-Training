public class averageOfSubjects {
    public static void main(String[] args) {
        // Marks of PCM
        int maths= 94;
        int physics= 95;
        int chemistry= 96;
        
        // calculating average of PCM
        double average= (maths+physics+chemistry)/ 3.0;
        System.out.println("Sam's average mark in PCM is "+ average);
    }
}